// AI Tool Reviews - Dynamic Content Loader

const ARTICLES_INDEX = '/js/articles.json';
let allArticles = [];
let currentCategory = 'all';

// DOM Elements
const articlesGrid = document.getElementById('articles-grid');
const filterButtons = document.querySelectorAll('.filter-btn');

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    loadArticles();
    initFilters();
});

// Load articles index and render
async function loadArticles() {
    try {
        const response = await fetch(ARTICLES_INDEX);
        if (!response.ok) throw new Error('Failed to load articles');

        allArticles = await response.json();
        renderArticles(allArticles);
    } catch (error) {
        console.error('Error loading articles:', error);
        // Fallback: try loading individual article list from data folder
        loadFromGeneratedFolder();
    }
}

// Load articles from generated output folder
async function loadFromGeneratedFolder() {
    // This is a fallback that scans the articles folder
    // In production, articles.json is generated by the publish script
    articlesGrid.innerHTML = '<p class="no-results">No reviews yet. Check back soon!</p>';
}

// Render articles to grid
function renderArticles(articles) {
    if (!articles || articles.length === 0) {
        articlesGrid.innerHTML = '<p class="no-results">No reviews in this category yet.</p>';
        return;
    }

    // Filter by category
    let filtered = articles;
    if (currentCategory !== 'all') {
        filtered = articles.filter(a => a.category === currentCategory);
    }

    if (filtered.length === 0) {
        articlesGrid.innerHTML = '<p class="no-results">No reviews in this category yet.</p>';
        return;
    }

    articlesGrid.innerHTML = filtered.map(article => `
        <a href="${article.url}" class="article-card">
            <div class="card-header">
                <span class="category-tag">${article.category}</span>
            </div>
            <div class="card-body">
                <h3>${article.name}</h3>
                <p>${article.excerpt || 'Click to read my honest experience with this tool.'}</p>
            </div>
            <div class="card-footer">
                <span class="read-more">Read Review →</span>
                <span class="date">${formatDate(article.date)}</span>
            </div>
        </a>
    `).join('');
}

// Initialize filter buttons
function initFilters() {
    filterButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            // Update active state
            filterButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            // Filter articles
            currentCategory = btn.dataset.category;
            renderArticles(allArticles);
        });
    });
}

// Format date
function formatDate(dateStr) {
    if (!dateStr) return '';
    const date = new Date(dateStr);
    return date.toLocaleDateString('en-US', {
        month: 'short',
        day: 'numeric',
        year: 'numeric'
    });
}

// Smooth scroll for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({ behavior: 'smooth' });
        }
    });
});